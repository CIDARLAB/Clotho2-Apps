    public void writeToPlasmidViewer(String s, Hashtable enz)
    {
        Enumeration keys = enz.keys();
        while (keys.hasMoreElements())
        {
            String enzyme = (String)keys.nextElement();
            String value = (String)enz.get(enzyme);
            String[] w = value.split("\\s+");
            String pattern=null;
            if (w.length==2 || w.length==1) pattern = w[0];
            else pattern = w[1];
            pattern = pattern.replaceAll("\\*", "");
            String regex = pattern.replaceAll("N", "[a-zA-Z]");
            regex ="\\["+enzyme+"\\]"+regex;
            pattern = "\\["+enzyme+"\\]"+pattern;
            
            s=s.replaceAll(regex,"<font color=\"red\">"+pattern+"</font>" );
        
        }
        String out = "<html>"+s+"</html>";
        this.JEditorPaneplasmidViewer.setText(out);
    }
