    public void writeOutput(boolean hasResult, ArrayList<String> frags, ArrayList<String> enzymes, ArrayList<String> locations)
    {
        String output ="";
        if (!hasResult)
        {
            this.jTextAreaOutput.setText("Sorry, none of the selected enzymes can be mapped on the plasmid!!");
        }

        else
        {
           for (int i = 0; i < enzymes.size(); i ++)
           {
               if (locations.get(i).length() > 2) //only show enzyme if it has been used
                output+="\nEnzyme  : "+enzymes.get(i)+ "\nRestriction site locations: "+locations.get(i)+"\n";
           }
           output+="\n Total number of fragments : "+frags.size()+"\n";
           output+="\n\n*****************************RESULTS FROM VIRTUAL DIGEST***************************************************\n\n ";
           for (int i =0; i < frags.size(); i++)
           {
               output+=frags.get(i).length()+" : "+frags.get(i)+"\n\n";
           }


           this.jTextAreaOutput.setText(output);

        }
    }
    
    
